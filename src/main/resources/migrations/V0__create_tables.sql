CREATE TABLE CLIENT (
    CLIENT_ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    CLIENT_DOCUMENT VARCHAR2(40) NOT NULL,
    CLIENT_NAME VARCHAR2(120) NOT NULL,
    COMPANY_EMAIL VARCHAR2(80) NOT NULL
);

CREATE TABLE CLIENT (
    CLIENT_ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    CNPJ VARCHAR2(30) NOT NULL UNIQUE,
    CLIENT_NAME VARCHAR2(120) NOT NULL,
    CONTACT_EMAIL VARCHAR2(80) NOT NULL UNIQUE,
    COMPANY_ID NUMBER NOT NULL,
    CONSTRAINT FK_CLIENT_COMPANY FOREIGN KEY (COMPANY_ID) REFERENCES COMPANY(COMPANY_ID)
);